/**
 * The full typed icon dictionary
 */
export const icons = iconsData

/**
 * Union type of all category names
 */
export type IconCategory = keyof typeof iconsData

/**
 * Union type of all icons (flat list)
 */
export type IconList = (typeof iconsData)[IconCategory][number]

/**
 * Returns all icons belonging to a specific category
 * @param category - The category name to filter by
 * @returns Array of icons in the specified category
 */
export function getIconsByCategory<T extends IconCategory>(
  category: T
): readonly (typeof iconsData)[T][number][] {
  return iconsData[category]
}

/**
 * Searches icons by matching against emoji or category name
 * @param term - The search term (case-insensitive)
 * @returns Array of matching icons
 */
export function searchIcons(term: string): IconList[] {
  const normalizedTerm = term.toLowerCase()
  const results: IconList[] = []
  const seen = new Set<string>()

  for (const [category, categoryIcons] of Object.entries(iconsData)) {
    const categoryMatches = category.toLowerCase().includes(normalizedTerm)

    for (const icon of categoryIcons) {
      if (seen.has(icon)) continue

      if (categoryMatches || icon.includes(term)) {
        results.push(icon as IconList)
        seen.add(icon)
      }
    }
  }

  return results
}

export default icons
